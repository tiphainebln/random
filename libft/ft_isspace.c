#include "libft.h"

int		ft_isspace(int c)
{
	c = (unsigned char)c;
	if (c == '\t' || c == '\n' || c == '\r' ||
			c == '\f' || c == '\v' || c == ' ')
		return (1);
	else
		return (0);
}
